package cn.smartx.push.model;

public class UserDevice {

	private int userId;
	private String deviceToken;
	private String smartxPlatform;
	private String uuid;
	private String appCurName;
	private String appPackageName;
	private String appCurVersion;
	private String appCurVersionNum;
	private String phoneModel;
	private String systemVersion;
	private String deviceSystem;
	private String manufacturer;
	
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public String getDeviceToken() {
		return deviceToken;
	}
	public void setDeviceToken(String deviceToken) {
		this.deviceToken = deviceToken;
	}
	public String getSmartxPlatform() {
		return smartxPlatform;
	}
	public void setSmartxPlatform(String smartxPlatform) {
		this.smartxPlatform = smartxPlatform;
	}
	public String getUuid() {
		return uuid;
	}
	public void setUuid(String uuid) {
		this.uuid = uuid;
	}
	public String getAppCurName() {
		return appCurName;
	}
	public void setAppCurName(String appCurName) {
		this.appCurName = appCurName;
	}
	public String getAppPackageName() {
		return appPackageName;
	}
	public void setAppPackageName(String appPackageName) {
		this.appPackageName = appPackageName;
	}
	public String getAppCurVersion() {
		return appCurVersion;
	}
	public void setAppCurVersion(String appCurVersion) {
		this.appCurVersion = appCurVersion;
	}
	public String getAppCurVersionNum() {
		return appCurVersionNum;
	}
	public void setAppCurVersionNum(String appCurVersionNum) {
		this.appCurVersionNum = appCurVersionNum;
	}
	public String getPhoneModel() {
		return phoneModel;
	}
	public void setPhoneModel(String phoneModel) {
		this.phoneModel = phoneModel;
	}
	public String getSystemVersion() {
		return systemVersion;
	}
	public void setSystemVersion(String systemVersion) {
		this.systemVersion = systemVersion;
	}
	public String getDeviceSystem() {
		return deviceSystem;
	}
	public void setDeviceSystem(String deviceSystem) {
		this.deviceSystem = deviceSystem;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	
	
	
}
