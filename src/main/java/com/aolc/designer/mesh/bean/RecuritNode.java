package com.aolc.designer.mesh.bean;

public class RecuritNode {
    private String category;
    private String address;
    private String educational_requirements;
    private String experience;
    private String job_detail;
    private String skills_requirements;
    private String compensation_benefit;
    private String tags;

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getEducational_requirements() {
        return educational_requirements;
    }

    public void setEducational_requirements(String educational_requirements) {
        this.educational_requirements = educational_requirements;
    }

    public String getExperience() {
        return experience;
    }

    public void setExperience(String experience) {
        this.experience = experience;
    }

    public String getJob_detail() {
        return job_detail;
    }

    public void setJob_detail(String job_detail) {
        this.job_detail = job_detail;
    }

    public String getSkills_requirements() {
        return skills_requirements;
    }

    public void setSkills_requirements(String skills_requirements) {
        this.skills_requirements = skills_requirements;
    }

    public String getCompensation_benefit() {
        return compensation_benefit;
    }

    public void setCompensation_benefit(String compensation_benefit) {
        this.compensation_benefit = compensation_benefit;
    }

    public String getTags() {
        return tags;
    }

    public void setTags(String tags) {
        this.tags = tags;
    }
}
